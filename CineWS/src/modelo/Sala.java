package modelo;

public class Sala {
	
	private int idSala;
	private String numSala;
	
	public int getIdSala() {
		return idSala;
	}
	public void setIdSala(int idSala) {
		this.idSala = idSala;
	}
	public String getNumSala() {
		return numSala;
	}
	public void setNumSala(String numSala) {
		this.numSala = numSala;
	}
	
}
