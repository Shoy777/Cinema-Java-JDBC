package modelo;

public class Cartelera {
	
	private int idCartelera;
	private String nomCartelera;
	private String idLocal;
	
	public int getIdCartelera() {
		return idCartelera;
	}
	public void setIdCartelera(int idCartelera) {
		this.idCartelera = idCartelera;
	}
	public String getNomCartelera() {
		return nomCartelera;
	}
	public void setNomCartelera(String nomCartelera) {
		this.nomCartelera = nomCartelera;
	}
	public String getIdLocal() {
		return idLocal;
	}
	public void setIdLocal(String idLocal) {
		this.idLocal = idLocal;
	}
	
}
